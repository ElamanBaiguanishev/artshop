import { SetMetadata } from '@nestjs/common';

export const IS_PUBLIC = 'isPublic';

/**
 * Помечает ручку как доступную без авторизации.
 *
 * Гуард висит глобально, поэтому по умолчанию всё закрыто: забытый декоратор
 * означает «эндпоинт недоступен» и обнаруживается сразу при разработке.
 * Обратная схема (закрывать вручную) означала бы, что забытый декоратор
 * оставляет дыру в проде, и узнаёшь ты о ней последним.
 */
export const Public = () => SetMetadata(IS_PUBLIC, true);
